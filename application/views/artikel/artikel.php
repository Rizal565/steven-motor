                <h2 class="mb-3">1. Siapkan Alat - Alat dan Bahan - Bahan yang Diperlukan</h2>
                <p>Untuk mengganti oli motor, Anda memerlukan beberapa alat dan bahan yang penting. Pertama, pastikan Anda memiliki oli baru yang sesuai dengan spesifikasi motor Anda. Selain itu, siapkan wadah penampung untuk menampung oli bekas yang akan dikeluarkan dari mesin. Anda juga memerlukan kunci pas atau kunci sok untuk membuka baut pembuangan oli. </p>
                <p class="mb-5">Kain lap atau tisu sangat berguna untuk membersihkan sisa-sisa oli yang mungkin tercecer. Jika diperlukan, gunakan corong untuk membantu menuangkan oli baru dengan lebih mudah. Jika motor Anda menggunakan filter oli yang perlu diganti, pastikan Anda memiliki kunci filter oli yang sesuai. Untuk melindungi tangan Anda dari kotoran dan oli, sarung tangan bisa menjadi pilihan tambahan yang berguna.</p>

                <h2 class="mb-3">2. Langkah - Langkah Mengganti Oli</h2>
                <p>Untuk mengganti oli motor, langkah pertama adalah mempersiapkan motor dan area kerja. Tempatkan motor di tempat yang datar dan stabil, gunakan standar tengah agar motor tidak mudah bergerak. Pastikan mesin dalam keadaan hangat untuk memastikan oli mengalir lebih mudah.</p>
                <p class="mb-4">Selanjutnya, buka tutup pengisian oli untuk membantu oli bekas mengalir lebih cepat. Siapkan wadah penampung oli bekas dan letakkan di bawah baut pembuangan oli.</p>
                <p class="mb-4">Lepaskan baut pembuangan oli menggunakan kunci pas atau kunci sok. Biarkan oli bekas mengalir sepenuhnya ke dalam wadah penampung. Jika motor dilengkapi dengan filter oli yang perlu diganti, gunakan kunci filter oli untuk melepaskan filter lama dan pasang filter baru dengan mengoleskan sedikit oli baru pada karet segel filter sebelum memasangnya.</p>
                <p class="mb-4">Setelah oli bekas habis, pasang kembali baut pembuangan oli dan kencangkan. Gunakan corong jika perlu untuk menuangkan oli baru melalui lubang pengisian oli, sesuai dengan kapasitas yang dianjurkan oleh pabrikan motor.</p>
                <p class="mb-4">Setelah mengisi oli, pasang kembali tutup pengisian oli. Hidupkan mesin selama beberapa menit agar oli baru tersebar merata, lalu matikan mesin dan tunggu beberapa menit. Periksa level oli menggunakan dipstick atau jendela pengamatan.</p>
                <p class="mb-4">Terakhir, bersihkan area kerja dari tumpahan oli. Buang oli bekas sesuai dengan peraturan yang berlaku. Dengan mengikuti langkah-langkah ini, Anda dapat mengganti oli motor dengan benar dan menjaga performa mesin tetap optimal.</p>
