                <h2 class="mb-3">Lakukan Service Secara Berkala</h2>
                <p class="mb-5">Servis berkala adalah perawatan rutin yang dilakukan di bengkel resmi atau bengkel terpercaya pada interval waktu atau jarak tempuh tertentu yang direkomendasikan oleh pabrikan motor. Tujuan dari servis berkala adalah untuk memastikan semua komponen motor berfungsi dengan baik dan untuk mencegah kerusakan yang lebih parah di masa depan. Berikut adalah beberapa langkah dan komponen yang biasanya diperiksa dan dirawat selama servis berkala:</p>
                <h3>Langkah Apa Saja yang Dilakukan Service Secara Berkala?</h3>
                <p class="mb-5">Perawatan motor yang menyeluruh sangat penting untuk menjaga kinerja dan umur panjang kendaraan Anda. Langkah pertama adalah pemeriksaan mesin, termasuk penggantian oli dan filter oli untuk memastikan pelumasan optimal serta pemeriksaan sistem pendingin jika motor menggunakan pendingin cairan. Selanjutnya, periksa sistem bahan bakar dengan menyetel dan membersihkan karburator atau memeriksa sistem injeksi, serta mengganti filter bahan bakar jika diperlukan.</p>
                <p class="mb-5">Sistem pengapian juga perlu diperiksa, termasuk kondisi busi dan kabel pengapian untuk memastikan percikan api optimal. Pemeriksaan sistem pengereman meliputi pengecekan kampas rem dan level cairan rem serta memastikan tidak ada kebocoran. Ketegangan dan pelumasan rantai harus diperiksa dan disetel sesuai spesifikasi pabrikan untuk mengurangi keausan.</p>
                <p class="mb-4">Sistem kelistrikan perlu dicek, termasuk kondisi baterai dan fungsi semua lampu serta klakson. Tekanan dan kondisi ban serta suspensi juga harus diperiksa untuk memastikan tidak ada kerusakan atau keausan yang tidak merata, serta memastikan suspensi berfungsi dengan baik. Terakhir, periksa kondisi fisik body motor dan sistem knalpot untuk memastikan tidak ada kerusakan atau kebocoran. Dengan perawatan rutin ini, motor Anda akan tetap dalam kondisi prima dan aman untuk digunakan.</p>
